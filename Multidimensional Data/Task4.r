# Времето на чакане (всекунди) на асансьор в сграда е равномерно разпределено
# в интервала (0 60). Ако засечем времето на чакане на 50 човека, каква е вероятността
# полученото средно време да е между 25 и 35 секунди?

b <- (35-30)/(60/(sqrt(12)*sqrt(50)))
a <- (25-30)/(60/(sqrt(12)*sqrt(50)))
pnorm(b) - pnorm(a)
