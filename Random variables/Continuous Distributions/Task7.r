# Обемът течност, който машина налива в една чаша, е нормално разпределен
# със средно 252 мл и стандартно отклонение 3 мл. Разглеждаме събитията:
# A={машината да налее в една чаша повече от 250 мл},
# B ={в най-много 2 от общо 5 наливания машината да налее повече от 250 мл}.
# Намерете вероятностите на събитията A и B като използвате вградените функции,
# свързани с разпределения.

