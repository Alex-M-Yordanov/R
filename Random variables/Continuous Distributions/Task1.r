# Генерирайте 500 случайни числа от равномерно разпределение в интервала
# (2 3). Начертайте хистограма на генерираните числа и на същата картинка добавете
# графика на плътността f(x). Повторете същото с 5000 случайни числа.

x<-runif(500,2,3)
hist(x)
hist(x,probability=T)
curve(dunif(x,2,3),from=2,to=3,add=T,lwd=2)
